package class03;

public class RecapOperators {

	public static void main(String[] args) {
		int number1=9;
		int number2=2;
		System.out.println(number1/number2);
		
		double number3=9; 
		double number4=2; 
		System.out.println(number3/number4);
		
		float number5=1.07f;
		float number6=3.0f; 
		System.out.println(number5/number6);
		
	}

}
